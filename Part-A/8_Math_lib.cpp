#include <iostream>
#include <cmath>
using namespace std;

int main() {
    double s = 25;
    cout << "Square root value of s=25 is: " << sqrt(s) << endl;

    int z = -10;
    cout << "Absolute value of z=-10 is: " << abs(z) << endl;

    int x = 4, y = 3;
    cout << "Power value x=4, y=3 is: " << pow(x, y) << endl;

    double m = 2.5;
    cout << "Floor value of m=2.5 is: " << floor(m) << endl;

    double n = 2.5;
    cout << "Ceiling value of n=2.5 is: " << ceil(n) << endl;

    return 0;
}
